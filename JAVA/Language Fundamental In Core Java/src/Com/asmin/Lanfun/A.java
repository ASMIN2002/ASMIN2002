package Com.asmin.Lanfun;

public class A {
	public static void main(String[] args) {
		int i = 0b10010;  // 0*2p0 + 1*2p1 + 0*2p2 + 0*2p3 + 1*2p4 = 2+16 = 18;
		System.out.println(i); // 18
		
		int j = 0B01001;   // 1*2p0 + 0*2p1 + 0*2p2 + 1*2p3 + 0*2p4 = 1+8 = 10
		System.out.println(j);
		

	}

}
